#include "MutantStack.hpp"

int	main(void)
{
	MutantStack<int> mstack;

	mstack.push(1);
	mstack.push(-2);

	std::cout << "Top: " << mstack.top() << std::endl;

	mstack.pop();

	std::cout << "Size: " << mstack.size() << std::endl;

	mstack.push(3);
	mstack.push(4);
	mstack.push(5);
	mstack.push(6);

	MutantStack<int>::iterator it = mstack.begin();
	MutantStack<int>::iterator ite = mstack.end();

	++it;
	--it;
	while (it != ite)
		std::cout << *it++ << " ";
	std::cout << std::endl;
	return (0);
}
